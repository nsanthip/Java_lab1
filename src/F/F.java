package F;

public class F {
    public static void main (String[] args){
        float x = 27.5f, y = 9.6f;
        System.out.println ("x=" +x);
        System.out.println ("y=" +y);
        System.out.println ("x+y=" +(x+y));
        System.out.println ("x-y=" +(x-y));
        System.out.println ("x*y=" +(x*y));
        System.out.println ("x/y=" +(x/y));
        System.out.println ("x%y=" +(x%y));
    }
}
